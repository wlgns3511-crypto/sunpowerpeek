def build():
    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)

    conn = sqlite3.connect(DB_PATH)
    c = conn.cursor()

    # Create tables
    c.execute('''CREATE TABLE states (
        state TEXT NOT NULL,
        abbr TEXT PRIMARY KEY,
        slug TEXT UNIQUE NOT NULL,
        avg_sun_hours REAL NOT NULL,
        avg_system_cost_per_watt REAL NOT NULL,
        federal_tax_credit_pct REAL NOT NULL,
        state_tax_credit REAL NOT NULL,
        state_rebate REAL NOT NULL,
        net_metering TEXT NOT NULL,
        avg_electricity_rate REAL NOT NULL,
        avg_monthly_bill REAL NOT NULL,
        avg_payback_years REAL NOT NULL,
        avg_20yr_savings REAL NOT NULL
    )''')

    c.execute('''CREATE TABLE zip_solar (
        zip_code TEXT PRIMARY KEY,
        city TEXT NOT NULL,
        state TEXT NOT NULL,
        slug TEXT UNIQUE NOT NULL,
        peak_sun_hours REAL NOT NULL,
        system_cost_6kw REAL NOT NULL,
        annual_production_kwh REAL NOT NULL,
        annual_savings REAL NOT NULL,
        payback_years REAL NOT NULL,
        co2_offset_tons REAL NOT NULL,
        FOREIGN KEY (state) REFERENCES states(abbr)
    )''')

    c.execute('''CREATE TABLE incentives (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        state TEXT NOT NULL,
        incentive_name TEXT NOT NULL,
        type TEXT NOT NULL,
        value TEXT NOT NULL,
        description TEXT NOT NULL,
        expiration TEXT
    )''')

    # Insert states
    for row in STATES_DATA:
        state, abbr, sun, cost, fed, stax, sreb, nm, erate, bill, payback, savings = row
        slug = slugify(state)
        c.execute('INSERT INTO states VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?)',
                  (state, abbr, slug, sun, cost, fed, stax, sreb, nm, erate, bill, payback, savings))

    # Insert ZIP codes
    seen_slugs = set()
    for row in ZIP_DATA:
        zc, city, state, sun, cost, prod, savings, payback, co2 = row
        slug = slugify(f"{zc}-{city}-{state}-solar")
        if slug in seen_slugs:
            slug = slug + '-2'
        seen_slugs.add(slug)
        c.execute('INSERT OR IGNORE INTO zip_solar VALUES (?,?,?,?,?,?,?,?,?,?)',
                  (zc, city, state, slug, sun, cost, prod, savings, payback, co2))

    # Insert incentives
    for row in INCENTIVES_DATA:
        state, name, itype, value, desc, exp = row
        c.execute('INSERT INTO incentives (state, incentive_name, type, value, description, expiration) VALUES (?,?,?,?,?,?)',
                  (state, name, itype, value, desc, exp))

    # Create indices
    c.execute('CREATE INDEX idx_states_slug ON states(slug)')
    c.execute('CREATE INDEX idx_zip_slug ON zip_solar(slug)')
    c.execute('CREATE INDEX idx_zip_state ON zip_solar(state)')
    c.execute('CREATE INDEX idx_zip_code ON zip_solar(zip_code)')
    c.execute('CREATE INDEX idx_incentives_state ON incentives(state)')
    c.execute('CREATE INDEX idx_states_sun ON states(avg_sun_hours)')
    c.execute('CREATE INDEX idx_states_payback ON states(avg_payback_years)')

    conn.commit()
    conn.close()

    print(f"Database built at {DB_PATH}")
    print(f"  States: {len(STATES_DATA)}")
    print(f"  ZIP codes: {len(ZIP_DATA)}")
    print(f"  Incentives: {len(INCENTIVES_DATA)}")


if __name__ == '__main__':
    build()
